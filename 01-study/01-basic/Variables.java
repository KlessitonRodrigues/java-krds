public class Variables {

    public static void main() {

        // Declaration of variables
        int counter = 0; // manual type
        var counter2 = 1; // auto indentified

        // 8 types of variables

        // Integer type variables
        byte num = 0; // width: 8 (-128 to 128)
        short num2 = 0; // width: 16 (-32k to 32k)
        int num3 = 0; // width: 32 (-2.1bi to 2.1bi)
        long num4 = 0; // width: 64

        boolean isNum = false;

        // Float type variables
        double num5 = 0.0000001;
        float num6 = 0.0000001F;

        // Character type variables
        char firstChar = 'A'; // primitive type
        String alfabet = "abc"; // Class String
    }
}
